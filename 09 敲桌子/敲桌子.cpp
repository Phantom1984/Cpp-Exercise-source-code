#include<iostream>
#include<string>
using namespace std;

/*------------------------------案例描述---------------------------------*/
/*从1开始数到数字100，如果数字个位含有7，或者数字十位含有7，或者该数字是7的倍数*/
/*我们打印敲桌子，其余数字直接打印输出*/

int main()
{
	int i;
	int a, b, c;
	for (i = 1; i <= 100; i++)
	{
		a = i % 10;			//数的个位
		b = i / 10 % 10;	//数的十位
		c = i % 7;          //数除以7的余数
		if (a == 7 || b == 7 || c == 0)
			cout << "敲桌子" << endl;
		else
			cout << i << endl;
	}
}